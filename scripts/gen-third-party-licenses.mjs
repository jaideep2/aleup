#!/usr/bin/env node
/**
 * Generates THIRD_PARTY_LICENSES.md from the licenses of all production
 * dependencies (including peerDependencies, which the host installs but we
 * still bundle knowledge of), plus packages that are bundled inside deps and
 * therefore invisible to the dependency graph (jsPDF/html2canvas inside
 * html2pdf.js, Open-Xml-PowerTools inside docxodus).
 *
 * CI regenerates this file and fails on diff, so it can never drift.
 */
import { execSync } from "node:child_process";
import { readFileSync, writeFileSync, readdirSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

// Bundled-inside / lineage entries that no license crawler can see.
const FORCED_ENTRIES = [
  {
    name: "jspdf",
    license: "MIT",
    note: "Bundled inside html2pdf.js (peer of @aleup/pdf).",
    homepage: "https://github.com/parallax/jsPDF",
  },
  {
    name: "html2canvas",
    license: "MIT",
    note: "Bundled inside html2pdf.js (peer of @aleup/pdf).",
    homepage: "https://github.com/niklasvh/html2canvas",
  },
  {
    name: "Open-Xml-PowerTools",
    license: "MIT",
    note: "Upstream of docxodus (peer of @aleup/docx), compiled to WASM.",
    homepage: "https://github.com/OpenXmlDev/Open-Xml-PowerTools",
  },
];

function collectDeclaredDeps() {
  const pkgsDir = join(root, "packages");
  const deps = new Map(); // name -> { requestedBy: Set, range }
  for (const dir of readdirSync(pkgsDir)) {
    const pkgPath = join(pkgsDir, dir, "package.json");
    if (!existsSync(pkgPath)) continue;
    const pkg = JSON.parse(readFileSync(pkgPath, "utf8"));
    for (const [name, range] of Object.entries({
      ...pkg.dependencies,
      ...pkg.peerDependencies,
    })) {
      if (name.startsWith("@aleup/")) continue;
      const entry = deps.get(name) ?? { requestedBy: new Set(), range };
      entry.requestedBy.add(pkg.name);
      deps.set(name, entry);
    }
  }
  return deps;
}

function licenseOf(name) {
  try {
    const out = execSync(`pnpm view ${name} license repository.url --json`, {
      cwd: root,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"],
    });
    const parsed = JSON.parse(out);
    return { license: parsed.license ?? "UNKNOWN", repo: parsed["repository.url"] ?? "" };
  } catch {
    // Offline or unpublished: resolve from the local install if present.
    try {
      const local = JSON.parse(
        readFileSync(join(root, "node_modules", name, "package.json"), "utf8"),
      );
      return { license: local.license ?? "UNKNOWN", repo: local.repository?.url ?? "" };
    } catch {
      return { license: "UNKNOWN", repo: "" };
    }
  }
}

const deps = collectDeclaredDeps();
const rows = [];
for (const [name, { requestedBy, range }] of [...deps.entries()].sort()) {
  const { license, repo } = licenseOf(name);
  rows.push({ name, range, license, repo, requestedBy: [...requestedBy].sort().join(", ") });
}

const lines = [
  "# Third-party licenses",
  "",
  "aleup depends on (as `dependencies` or `peerDependencies`) the following",
  "packages. Each retains its own copyright and license; this file is the",
  "aggregated NOTICE required by those licenses. Generated by",
  "`scripts/gen-third-party-licenses.mjs` — do not edit by hand.",
  "",
  "| Package | Range | License | Used by | Repository |",
  "|---|---|---|---|---|",
  ...rows.map(
    (r) => `| ${r.name} | \`${r.range}\` | ${r.license} | ${r.requestedBy} | ${r.repo} |`,
  ),
  "",
  "## Bundled / upstream lineage",
  "",
  ...FORCED_ENTRIES.map((e) => `- **${e.name}** (${e.license}) — ${e.note} ${e.homepage}`),
  "",
];

const unknown = rows.filter((r) => r.license === "UNKNOWN");
writeFileSync(join(root, "THIRD_PARTY_LICENSES.md"), lines.join("\n"));
console.log(`THIRD_PARTY_LICENSES.md written (${rows.length} packages).`);
if (unknown.length) {
  console.error(`UNKNOWN licenses for: ${unknown.map((r) => r.name).join(", ")}`);
  process.exit(1);
}
